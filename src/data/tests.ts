export interface TestQuestion {
  id: string
  text: string
  dimension?: string
}

export interface TestDef {
  id: string
  title: string
  description: string
  questionCount: number
  questions: TestQuestion[]
}

function toQuestions(prefix: string, texts: string[], dimension?: string): TestQuestion[] {
  return texts.map((text, i) => ({
    id: `${prefix}-q${i + 1}`,
    text,
    dimension: dimension || 'general',
  }))
}

